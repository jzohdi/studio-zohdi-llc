import { spawn } from 'node:child_process';
import { access, mkdir, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const workspaceRoot = fileURLToPath(new URL('..', import.meta.url));
const remotionOutDir = path.join(workspaceRoot, 'remotion', 'out');
const staticPreviewsDir = path.join(workspaceRoot, 'static', 'project-previews');
const generatedManifestPath = path.join(
	workspaceRoot,
	'src',
	'lib',
	'generated',
	'project-preview-media.ts'
);

const args = process.argv.slice(2);
const projectIds = args.filter((arg) => !arg.startsWith('--'));
const skipRender = args.includes('--skip-render');

if (projectIds.length === 0) {
	console.error(
		'Usage: node scripts/publish-project-preview.mjs <project-id> [project-id...] [--skip-render]'
	);
	process.exit(1);
}

await mkdir(remotionOutDir, { recursive: true });
await mkdir(staticPreviewsDir, { recursive: true });

for (const projectId of projectIds) {
	await publishProjectPreview(projectId);
}

await writePreviewManifest();

console.info(`Updated preview manifest at ${toWorkspaceRelativePath(generatedManifestPath)}`);

async function publishProjectPreview(projectId) {
	const compositionId = `ProjectPreview-${projectId}`;
	const rawOutputPath = path.join(remotionOutDir, `${projectId}.mp4`);
	const publishedDir = path.join(staticPreviewsDir, projectId);
	const mp4Path = path.join(publishedDir, 'preview.mp4');
	const webmPath = path.join(publishedDir, 'preview.webm');
	const posterPath = path.join(publishedDir, 'poster.jpg');

	await mkdir(publishedDir, { recursive: true });

	if (!skipRender) {
		console.info(`Rendering ${compositionId} to ${toWorkspaceRelativePath(rawOutputPath)}...`);

		await runCommand(
			'npm',
			['--prefix', 'remotion', 'run', 'render', '--', compositionId, rawOutputPath],
			workspaceRoot
		);
	} else {
		await assertFileExists(
			rawOutputPath,
			`Missing raw render at ${toWorkspaceRelativePath(rawOutputPath)}. Run the command without --skip-render or render the composition first.`
		);
	}

	console.info(`Optimizing MP4 for ${projectId}...`);

	await runCommand(
		'ffmpeg',
		[
			'-y',
			'-i',
			rawOutputPath,
			'-an',
			'-c:v',
			'libx264',
			'-preset',
			'slow',
			'-crf',
			'23',
			'-pix_fmt',
			'yuv420p',
			'-movflags',
			'+faststart',
			mp4Path
		],
		workspaceRoot
	);

	console.info(`Encoding WebM for ${projectId}...`);

	await runCommand(
		'ffmpeg',
		[
			'-y',
			'-i',
			rawOutputPath,
			'-an',
			'-c:v',
			'libvpx-vp9',
			'-pix_fmt',
			'yuv420p',
			'-b:v',
			'0',
			'-crf',
			'36',
			'-deadline',
			'good',
			'-cpu-used',
			'2',
			'-row-mt',
			'1',
			webmPath
		],
		workspaceRoot
	);

	console.info(`Capturing first-frame poster for ${projectId}...`);

	await runCommand(
		'ffmpeg',
		['-y', '-i', mp4Path, '-frames:v', '1', '-q:v', '2', '-update', '1', posterPath],
		workspaceRoot
	);

	console.info(`Published assets to ${toWorkspaceRelativePath(publishedDir)}`);
}

async function writePreviewManifest() {
	await mkdir(path.dirname(generatedManifestPath), { recursive: true });

	const directories = await readdir(staticPreviewsDir, { withFileTypes: true }).catch((error) => {
		if (isMissingFileError(error)) {
			return [];
		}

		throw error;
	});

	const manifestEntries = [];

	for (const directory of directories.filter((entry) => entry.isDirectory()).sort(sortByName)) {
		const projectId = directory.name;
		const projectDir = path.join(staticPreviewsDir, projectId);
		const posterFilePath = path.join(projectDir, 'poster.jpg');
		const mp4FilePath = path.join(projectDir, 'preview.mp4');
		const webmFilePath = path.join(projectDir, 'preview.webm');

		if (!(await fileExists(posterFilePath)) || !(await fileExists(mp4FilePath))) {
			continue;
		}

		const sources = [];

		if (await fileExists(webmFilePath)) {
			sources.push({
				path: toPublicAssetPath(path.join('project-previews', projectId, 'preview.webm')),
				type: 'video/webm'
			});
		}

		sources.push({
			path: toPublicAssetPath(path.join('project-previews', projectId, 'preview.mp4')),
			type: 'video/mp4'
		});

		manifestEntries.push([
			projectId,
			{
				posterPath: toPublicAssetPath(path.join('project-previews', projectId, 'poster.jpg')),
				sources
			}
		]);
	}

	const manifest = Object.fromEntries(manifestEntries);
	const manifestSource = [
		'export type ProjectPreviewSource = {',
		'\tpath: string;',
		"\ttype: 'video/webm' | 'video/mp4';",
		'};',
		'',
		'export type ProjectPreviewAsset = {',
		'\tposterPath: string;',
		'\tsources: ProjectPreviewSource[];',
		'};',
		'',
		'// Generated by scripts/publish-project-preview.mjs',
		`export const projectPreviewMedia: Record<string, ProjectPreviewAsset> = ${JSON.stringify(manifest, null, 2)};`,
		'',
		'export function getProjectPreviewMedia(projectId: string): ProjectPreviewAsset | null {',
		'\treturn projectPreviewMedia[projectId] ?? null;',
		'}',
		''
	].join('\n');

	await writeFile(generatedManifestPath, manifestSource);
}

async function runCommand(command, commandArgs, cwd) {
	await new Promise((resolve, reject) => {
		const childProcess = spawn(command, commandArgs, {
			cwd,
			stdio: 'inherit',
			env: process.env
		});

		childProcess.on('error', reject);
		childProcess.on('exit', (code) => {
			if (code === 0) {
				resolve();
				return;
			}

			reject(
				new Error(
					`Command failed (${command} ${commandArgs.join(' ')}) with exit code ${code ?? 'unknown'}`
				)
			);
		});
	});
}

async function assertFileExists(filePath, message) {
	if (await fileExists(filePath)) {
		return;
	}

	throw new Error(message);
}

async function fileExists(filePath) {
	try {
		await access(filePath);
		return true;
	} catch (error) {
		if (isMissingFileError(error)) {
			return false;
		}

		throw error;
	}
}

function sortByName(a, b) {
	return a.name.localeCompare(b.name);
}

function toPublicAssetPath(relativePath) {
	return `/${relativePath.split(path.sep).join('/')}`;
}

function toWorkspaceRelativePath(targetPath) {
	return path.relative(workspaceRoot, targetPath) || '.';
}

function isMissingFileError(error) {
	return Boolean(error) && typeof error === 'object' && 'code' in error && error.code === 'ENOENT';
}
